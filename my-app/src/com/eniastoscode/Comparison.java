package com.eniastoscode;

public class Comparison {
    public static void main (String[] args){
        // Comparison operators
        int eniasAge = 20;
        int milkaAge = 20;
        System.out.println(eniasAge > milkaAge);
        System.out.println(eniasAge >= milkaAge);
        System.out.println(eniasAge < milkaAge);
        System.out.println(eniasAge <= milkaAge);
        System.out.println(eniasAge == milkaAge);
        System.out.println(eniasAge != milkaAge);

    }
}
